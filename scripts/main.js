$(document).ready(function() {
	
	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////
	////////////////////////////////////////// THIS IS THE BEGINNING
	
	$(function() {
		$('nav ul li a[href*="#"]:not([href="#"])').click(function() {
			if (location.pathname.replace(/^\//,'') == this.pathname.replace(/^\//,'') && location.hostname == this.hostname) {
				var target = $(this.hash);
				target = target.length ? target : $('[name=' + this.hash.slice(1) +']');
				if (target.length) {
					$('html, body').animate({
						scrollTop: target.offset().top - 50
					}, 1000);
					return false;
				}
			}
		});
	});
	
	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////
	
	$(function(){
		$('.toggle').click(function () {
			$(this).text(function(i, text){
				return text === '✘ 余分な熟語を隠します' ? '✔ 余分な熟語を表示します' : '✘ 余分な熟語を隠します';
			})
			if($(this).attr('title') == 'Hide Extra Compound Words') {
				$(this).attr('title', 'Show Extra Compound Words');
				$('.word.extra').css('display', 'none');
				$('.word.extra + br').hide();
			} else {
				$(this).attr('title', 'Hide Extra Compound Words');
				$('.word.extra').css('display', 'inline');
				$('.word.extra + br').show();
			}
		});
	})
	
	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////
	
	$('.navbar-brand').click(function () {
		$('html, body').animate({scrollTop: 0}, 1000);
	});
	
	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////
	
	$('body').removeClass('hide').addClass('show');
	
	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////
	//////////////////////////////////////////////// THIS IS THE END
	
});
